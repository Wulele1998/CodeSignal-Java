package core;

public class SwapAdjacentBits {
    int solution(int n) {
        // 13 = 1101
        // 13 << 1 => 011010
        // 13 << 1 & 0b10101010101010101010101010101010 => 001010
        // 13 >> 1 => 0110
        // 13 >> 1 & 0b01010101010101010101010101010101 => 0100
        return (n << 1 & 0b10101010101010101010101010101010) + (n >> 1 & 0b01010101010101010101010101010101);
    }
}
